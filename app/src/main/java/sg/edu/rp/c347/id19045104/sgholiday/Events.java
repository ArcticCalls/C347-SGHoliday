package sg.edu.rp.c347.id19045104.sgholiday;

public class Events {

    private String eventName;
    private String Date;

    public Events(String eventName, String date) {
        this.eventName = eventName;
        Date = date;
    }

    public String getEventName() {
        return eventName;
    }

    public String getDate() {
        return Date;
    }
}
